// to find given character is vowel or consonant using switch case
// only lower case allow...

const character = "a";

// console.log("only lower case allow");

switch (character) {
    case 'a':
        console.log("vowel");
        break;
    case 'e':
        console.log("vowel");
        break;
    case 'i':
        console.log("vowel");
        break;
    case 'o':
        console.log("vowel");
        break;
    case 'u':
        console.log("vowel");
        break;
    default :
        console.log("consonant");
}